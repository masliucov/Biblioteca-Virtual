    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String isbn = jTextField1.getText().trim();
        if (isbnExiste(isbn)) {
            JOptionPane.showMessageDialog(this, "ISBN jÃ¡ registrado. Por favor, insira um ISBN Ãºnico.", "Erro de ISBN", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Coleta os dados nis jTextField/jCamoBox
        String nome = jTextField2.getText().trim();
        String autor = jTextField3.getText().trim();
        String categoriaNome = (String) jComboBox1.getSelectedItem();
        String imagem = jTextField4.getText().trim(); // Opcional
        int copias;

        try {
            copias = Integer.parseInt(jTextField5.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "NÃºmero de cÃ³pias deve ser um nÃºmero inteiro.", "Erro de Formato", JOptionPane.ERROR_MESSAGE);
            return; // Encerra a execuÃ§Ã£o se o nÃºmero de cÃ³pias nÃ£o for vÃ¡lido
        }

        // ObtenÃ§Ã£o do ID da categoria a partir do nome
        int idCategoria = obterIdCategoria(categoriaNome);
        if (idCategoria == -1) {
            JOptionPane.showMessageDialog(this, "Categoria nÃ£o encontrada.", "Erro", JOptionPane.ERROR_MESSAGE);
            return; // Encerra a execuÃ§Ã£o se a categoria nÃ£o for encontrada
        }

        // Preparando a consulta SQL para inserÃ§Ã£o
        String query = "INSERT INTO livro (isbn, nome, autor, id_categoria, imagem, copias) VALUES (?, ?, ?, ?, ?, ?)";

        try (Connection conn = DatabaseUtil.getConnection(); PreparedStatement pstmt = conn.prepareStatement(query)) {

            pstmt.setString(1, isbn);
            pstmt.setString(2, nome);
            pstmt.setString(3, autor);
            pstmt.setInt(4, idCategoria);
            if (!imagem.isEmpty()) {
                pstmt.setString(5, imagem);
            } else {
                pstmt.setNull(5, java.sql.Types.VARCHAR);
            }
            pstmt.setInt(6, copias);

            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Livro adicionado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao adicionar livro: " + e.getMessage(), "Erro de SQL", JOptionPane.ERROR_MESSAGE);
        }
    }

    private boolean isbnExiste(String isbn) {
        String query = "SELECT isbn FROM livro WHERE isbn = ?";
        try (Connection conn = DatabaseUtil.getConnection(); PreparedStatement pstmt = conn.prepareStatement(query)) {
            pstmt.setString(1, isbn);
            ResultSet rs = pstmt.executeQuery();
            if (rs.next()) {
                return true; // ISBN jÃ¡ existe no banco de dados
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao verificar o ISBN: " + e.getMessage(), "Erro de SQL", JOptionPane.ERROR_MESSAGE);
        }
        return false; // ISBN nÃ£o encontrado, seguro para inserÃ§Ã£o
    }

    private int obterIdCategoria(String nomeCategoria) {
        String query = "SELECT id_categoria FROM categoria WHERE nome = ?";
        try (Connection conn = DatabaseUtil.getConnection(); PreparedStatement pstmt = conn.prepareStatement(query)) {

            pstmt.setString(1, nomeCategoria);
            ResultSet rs = pstmt.executeQuery();

            if (rs.next()) {
                return rs.getInt("id_categoria");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao procurar categoria: " + e.getMessage(), "Erro de SQL", JOptionPane.ERROR_MESSAGE);
        }
        return -1; // Retorna -1 se a categoria nÃ£o for encontrada
    }//GEN-LAST:event_jButton1ActionPerformed

