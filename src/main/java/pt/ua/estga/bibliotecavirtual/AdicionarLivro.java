    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String isbn = inserirISBN.getText().trim();
        if (isbnExiste(isbn)) {
            JOptionPane.showMessageDialog(this, "ISBN já registrado. Por favor, insira um ISBN único.", "Erro de ISBN", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String nome = inserirNome.getText().trim();
        String autor = inserirAutor.getText().trim();
        String categoriaNome = (String) escolherCategoria.getSelectedItem();
        String imagem = inserirImagem.getText().trim();
        int copias;

        try {
            copias = Integer.parseInt(inserirCopias.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Número de cópias deve ser um número inteiro.", "Erro de Formato", JOptionPane.ERROR_MESSAGE);
            return; // encerra a execução se o número de cópias não for válido
        }

        // obtem o ID da categoria a partir do nome
        int idCategoria = obterIdCategoria(categoriaNome);
        if (idCategoria == -1) {
            JOptionPane.showMessageDialog(this, "Categoria não encontrada.", "Erro", JOptionPane.ERROR_MESSAGE);
            return; // encerra a execução se a categoria não for encontrada
        }

        // consulta SQL para inserção
        String query = "INSERT INTO livro (isbn, nome, autor, id_categoria, imagem, copias) VALUES (?, ?, ?, ?, ?, ?)";

        try (Connection conn = DatabaseUtil.getConnection(); PreparedStatement pstmt = conn.prepareStatement(query)) {

            pstmt.setString(1, isbn);
            pstmt.setString(2, nome);
            pstmt.setString(3, autor);
            pstmt.setInt(4, idCategoria);
            if (!imagem.isEmpty()) {
                pstmt.setString(5, imagem);
            } else {
                pstmt.setNull(5, java.sql.Types.VARCHAR);
            }
            pstmt.setInt(6, copias);

            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Livro adicionado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao adicionar livro: " + e.getMessage(), "Erro de SQL", JOptionPane.ERROR_MESSAGE);
        }
    }

    private boolean isbnExiste(String isbn) {
        String query = "SELECT isbn FROM livro WHERE isbn = ?";
        try (Connection conn = DatabaseUtil.getConnection(); PreparedStatement pstmt = conn.prepareStatement(query)) {
            pstmt.setString(1, isbn);
            ResultSet rs = pstmt.executeQuery();
            if (rs.next()) {
                return true; // ISBN já existe na base de dados
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao verificar o ISBN: " + e.getMessage(), "Erro de SQL", JOptionPane.ERROR_MESSAGE);
        }
        return false; // ISBN não encontrado
    }

    private int obterIdCategoria(String nomeCategoria) {
        String query = "SELECT id_categoria FROM categoria WHERE nome = ?";
        try (Connection conn = DatabaseUtil.getConnection(); PreparedStatement pstmt = conn.prepareStatement(query)) {

            pstmt.setString(1, nomeCategoria);
            ResultSet rs = pstmt.executeQuery();

            if (rs.next()) {
                return rs.getInt("id_categoria");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao procurar categoria: " + e.getMessage(), "Erro de SQL", JOptionPane.ERROR_MESSAGE);
        }
        return -1; // Retorna -1 se a categoria não for encontrada
    }//GEN-LAST:event_jButton1ActionPerformed

