    private void carregarDetalhesCompra(int idCarrinho) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        double total = 0.0;

        String query = "SELECT l.isbn, l.nome, c.preco, c.data "
                + "FROM compra c JOIN livro l ON c.id_livro = l.id_livro "
                + "WHERE c.id_carrinho = ?";
        try (Connection conn = DatabaseUtil.getConnection(); PreparedStatement stmt = conn.prepareStatement(query)) {
            stmt.setInt(1, idCarrinho);
            ResultSet rs = stmt.executeQuery();
            while (rs.next()) {
                double preco = rs.getDouble("preco");
                total += preco; // adiciona o preço ao total
                model.addRow(new Object[]{rs.getString("isbn"), rs.getString("nome"), preco, rs.getTimestamp("data")});
            }
            jTotal.setText(String.format("€%.2f", total)); // atualiza o jTotal com o preço total
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar detalhes da compra: " + e.getMessage(), "Erro da Base de Dados", JOptionPane.ERROR_MESSAGE);
        }
    }

